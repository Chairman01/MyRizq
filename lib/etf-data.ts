export function filterETFsByCategory(category: string): ETF[] {
  if (category === "all") return etfData
  if (category === "us") return etfData.filter(etf => etf.focus.includes("US") && !etf.focus.includes("Ex-US"))
  if (category === "global") return etfData.filter(etf => (etf.focus.includes("Global") || etf.focus.includes("Ex-US")) && !etf.focus.includes("Real Estate") && !etf.focus.includes("Technology"))
  if (category === "emerging") return etfData.filter(etf => etf.focus.includes("Emerging"))
  if (category === "fixed-income") return etfData.filter(etf => etf.focus.includes("Sukuk"))
  if (category === "real-estate") return etfData.filter(etf => etf.focus.includes("Real Estate"))
  if (category === "technology") return etfData.filter(etf => etf.focus.includes("Technology") && !etf.focus.includes("Ex-US") && !etf.focus.includes("Global Large"))
  return etfData
}

export function getRiskColor(risk: "Low" | "Moderate" | "High"): string {
  switch (risk) {
    case "Low": return "text-green-600"
    case "Moderate": return "text-yellow-600"
    case "High": return "text-red-600"
    default: return "text-muted-foreground"
  }
}
